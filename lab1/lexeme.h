#pragma once

#include <string>

using namespace std;

//Стуркутра, являющаяся програмным предстовлением токена.
struct lexeme {
	string name; //имя индендификатора
	int type;
	//Тип индендификатора
	//0 - не опрерделён
	//1 - int
	//2 - char
	//3 - float
	bool isInit; //Инциализирован ли индедификатор. true - инициализирован, false - нет.

	//Констукторы, для уменьшения кода
	lexeme() {
	}
	lexeme(string elemName) {
		name = elemName;
		type = 0;
		isInit = false;
	}

	//Для сета
	bool operator < (const lexeme& b) const {
		return name < b.name;
	}

	bool operator == (lexeme b) {
		return name == b.name;
	}
};

